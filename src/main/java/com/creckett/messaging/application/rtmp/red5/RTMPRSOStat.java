package com.creckett.messaging.application.rtmp.red5;

public class RTMPRSOStat {
	private String rsoName;
	private int version;
	
	
	public RTMPRSOStat(String rsoName, int version) {
		super();
		this.rsoName = rsoName;
		this.version = version;
	}
	public String getRsoName() {
		return rsoName;
	}
	public void setRsoName(String rsoName) {
		this.rsoName = rsoName;
	}
	public int getVersion() {
		return version;
	}
	public void setVersion(int version) {
		this.version = version;
	}
	
	
}
