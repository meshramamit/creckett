package com.creckett.core.codec;

/**
 * This interface contains methods for encoding/decoding mechanism. Concrete
 * class implementing this interface will need to provide implementation of
 * these methods as per supported protocol.
 * 
 * @author vkarmani
 * 
 */
public interface CodecStrategy {

	Object encode(Object objectToEncode);
	
	Object decode(Object objectToDecode);
}
