package com.creckett.dto;

import java.util.HashMap;
import java.util.Map;

public class SessionWiseArchiveBets {

	private Integer session;
	
	private Map<String, ArchiveBet> bets = new HashMap<String, ArchiveBet>();
	
	private Map<String, String> users = new HashMap<String, String>();

	public Integer getSession() {
		return session;
	}

	public void setSession(Integer session) {
		this.session = session;
	}

	public Map<String, ArchiveBet> getBets() {
		return bets;
	}

	public void setBets(Map<String, ArchiveBet> bets) {
		this.bets = bets;
	}

	public Map<String, String> getUsers() {
		return users;
	}

	public void setUsers(Map<String, String> users) {
		this.users = users;
	}
}
