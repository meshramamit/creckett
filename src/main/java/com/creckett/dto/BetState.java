package com.creckett.dto;

import java.util.List;

public class BetState {

	private String over;
	
	private MatchScore overScore;
	
	private int groupOverId;
	
	private UserBetState winningBet;
	
	private String winners = "";

	private List<UserBetState> userBetStates;

	public String getOver() {
		return over;
	}

	public void setOver(String over) {
		this.over = over;
	}

	public List<UserBetState> getUserBetStates() {
		return userBetStates;
	}

	public void setUserBetStates(List<UserBetState> userBetStates) {
		this.userBetStates = userBetStates;
	}

	public int getGroupOverId() {
		return groupOverId;
	}

	public void setGroupOverId(int groupOverId) {
		this.groupOverId = groupOverId;
	}

	public UserBetState getWinningBet() {
		return winningBet;
	}

	public void setWinningBet(UserBetState winningBet) {
		this.winningBet = winningBet;
	}

	public MatchScore getOverScore() {
		return overScore;
	}

	public void setOverScore(MatchScore overScore) {
		this.overScore = overScore;
	}

	public String getWinners() {
		return winners;
	}

	public void setWinners(String winners) {
		this.winners = winners;
	}

}
